public class codding7 {
    // Это функция, которая принимает имя и ничего не возвращает
    public static void sayHello(String name) {
        System.out.println("Привет, " + name + "!");
    }

    public static void main(String[] args) {
        // Вызов функции
        sayHello("Алия");
        sayHello("Ильдар");
        sayHello("Bayel");
    }
}
